function generateRecept(orders){
    let receipt ='';
    orders.map(order => {
        // latte: 2 x 3 =6
        const orderName = order.name.replace("-order","");
        const orderprice= document.getElementById("pricing").elements.namedItem(orderName).value;
        const orderValue = orderprice *  order.value;
        total += orderValue ;
        receipt += order.name + " : " + order.value + "x" + orderprice + " = $"+ orderValue +"." +"\n";
    })
     return receipt;
}
